package VO;

public class ADVO {
	private int hub_id;
	private int no_of_server;
	private String hub_status;
	
	public int getHub_id() {
		return hub_id;
	}
	public void setHub_id(int hub_id) {
		this.hub_id = hub_id;
	}
	public int getNo_of_server() {
		return no_of_server;
	}
	public void setNo_of_server(int no_of_server) {
		this.no_of_server = no_of_server;
	}
	public String getHub_status() {
		return hub_status;
	}
	public void setHub_status(String hub_status) {
		this.hub_status = hub_status;
	}
	
	
}
